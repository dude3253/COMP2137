#!/bin/bash

userName=$USER
today=$(date +%y-%m-%d/%l:%M:%S)
comp_hostname=$(hostname)
userUptime=$(uptime)
cpuInfo=$(lscpu | grep -i 'Model name:' | cut -d':' -f2 | tr -d '[:space:]')
cpuSpeed=$(cat /proc/cpuinfo | grep -i 'cpu mhz' | head -n 1 | awk '{print $4}')
ramSize=$(free -h | grep -i "mem:" | awk '{print $2}')
osSource=$(cat /etc/os-release | grep "PRETTY_NAME" | cut -d= -f2)
disks=$(lsblk -dno model | tr -d '[:space:]' | cut -dS -f2)
diskSize=$(lsblk -dno size | tail -n 3 | awk '{print $1}')
vidCardMake=$(lshw -class display | grep 'vendor:' | cut -d':' -f2)
vidCard=$(lshw -class display | grep -i 'product:' | cut -d':' -f2)
ipAddress=$(ip addr show dev ens33 | awk '/inet /{print $2}')
fqdn=$(hostname -f)
hostAddress=$(ip a s dev ens33 | awk '/inet /{print $2}' | cut -d'/' -f1)
gatewayIP=$(ip route | awk '/default /{print $3}')


cat <<EOF

System Report generated by $userName, $today
 
 
System Information
------------------
HOSTNAME: $comp_hostname
OS: $osSource
Uptime: $userUptime

Hardware Information
--------------------
CPU: $cpuInfo
Speed: $cpuSpeed
RAM: $ramSize
Disks: $disks$diskSize
Videocard: $vidCardMake$vidCard

Network Information
-------------------
FQDN: $fqdn
Host Address: $hostAddress
Gateway IP: $gatewayIP
DNS Server:
Interface Name: 
IP Address: $ipAddress

System Status
-------------
Users Logged In: 
Disk Space: 
Process Count: 
Load Averages: 
Memory Allocation: 
Listening Network Ports: 
UFW Rules: 

EOF
